//Equal to
var a = 10;
console.log(a == 10);
console.log(a == 20);

//Identical
console.log(a === 10);
console.log(a === 20);

//Not equal to
console.log(a != 10);
console.log(a != 20);

//Not identical
console.log(a !== 10);
console.log(a !== 5);

//Greater than
console.log(a > 11);
console.log(a > 9);

//Greater than or equal to
console.log(a >= 10);
console.log(a >= 15);
console.log(a >= 9);

//Less than
console.log(a < 5);
console.log(a < 15);

//Less than or equal to
console.log(a <= 10);
console.log(a <= 5);
console.log(a <= 15);
